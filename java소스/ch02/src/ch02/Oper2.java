package ch02;

public class Oper2 {
	public static void main(String[] args) {
		int x,y;
		x = 10; y = 5;
		System.out.println("x + y = "+(x + y));
		System.out.println("x - y = "+(x - y));
		System.out.println("x * y = "+(x * y));
		System.out.println("x / y = "+(x / y));
		System.out.println("x % y = "+(x % y));
		System.out.println("x++ = "+(x++));
		System.out.println("y++ = "+(y++));
		x=10; y=5;
		System.out.println("++x = "+(++x));
		System.out.println("++y = "+(++y));

	}
}
