package ch02;

public class Subs3 {
	public static void main(String[] args) {
		int x = 8, y = 4;
		x += y;
		System.out.println("x=" + x + ", y = " + y);
		x -= y;
		System.out.println("x=" + x + ", y = " + y);
		x *= y;
		System.out.println("x=" + x + ", y = " + y);
		x /= y;
		System.out.println("x=" + x + ", y = " + y);
		x %= y;
		System.out.println("x=" + x + ", y = " + y);
	}
}
