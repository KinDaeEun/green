package ch02;

public class Arith1 {
	public static void main(String[] args) {
		int i1 = 12, i2 = 5;
		System.out.println("i1 + i2 = "+(i1 + i2));
		System.out.println("i1 - i2 = "+(i1 - i2));
		System.out.println("i1 * i2 = "+(i1 * i2));
		System.out.println("i1 / i2 = "+(i1 / i2));
		System.out.println("i1 % i2 = "+(i1 % i2));
	}
}
