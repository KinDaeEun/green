package ch02;

public class Arith3 {
	public static void main(String[] args) {
		int i1 = 7;
		double d1 = 3.14;
		System.out.println("i1 + d2 = "+(i1 + d1));
		System.out.println("i1 - d2 = "+(i1 - d1));
		System.out.println("i1 * d2 = "+(i1 * d1));
		System.out.println("i1 / d2 = "+(i1 / d1));
		System.out.println("i1 % d2 = "+(i1 % d1));
	}
}
