package ch02;

public class Arith2 {
	public static void main(String[] args) {
		int i1 = 27, i2 = 4;
		System.out.println("i1 + i2 = "+(i1 + i2));
		System.out.println("i1 - i2 = "+(i1 - i2));
		System.out.println("i1 * i2 = "+(i1 * i2));
		System.out.println("i1 / i2 = "+((double)i1 / i2));
		System.out.println("i1 % i2 = "+(i1 % i2));
	}
}
