package ch02;

public class Oper6 {
	public static void main(String[] args) {
		char c1 = 'a';
		System.out.println("c1 = " + c1);
		char c2 = 'a' + 1;
		System.out.println("c2 = " + c2);
		char c3 = ++c1;
		System.out.println("c3 = " + c3);

		int i1 = 'C' - 'A';
		System.out.println("i1 = " + i1);
		int i2 = '5';
		System.out.println("i2 = " + i2);
	}
}
