package ch02;
public class Var2 {
	public static void main(String[] args) {
//		정수 i1정의
		int i1;
//		66을 대입하고 출력
		i1 = 66;
		System.out.println("i = "+i1);
//		byte형으로 b1 88을 대입하고 출력
		byte b1 = 88;
		System.out.println("b1 = " + b1);
//		double d1에 3.14를 대입하고 출력
		double d1 = 3.14;
		System.out.println("d1 = " + d1);
	}
}